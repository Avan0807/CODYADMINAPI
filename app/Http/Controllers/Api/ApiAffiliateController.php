            // ðŸ›‘ Náº¿u Ä‘Ã£ click gáº§n Ä‘Ã¢y => KhÃ´ng cá»™ng Ä‘iá»ƒm
            $pointsAdded = 0;
        }

        return response()->json([
            'message' => 'Click Ä‘Æ°á»£c ghi nháº­n!',
